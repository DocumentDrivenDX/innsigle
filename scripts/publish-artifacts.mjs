#!/usr/bin/env node
/**
 * Publish docs/helix (+ glossary sources) into docs/website/content/generated/.
 * Pattern adapted from HELIX scripts/publish-artifacts.py.
 *
 * Curated copy lives in docs/website/content/curated/ (outside docs/helix).
 * Generated tree is wiped and rebuilt each run (deterministic, idempotent).
 */
import { createHash } from "node:crypto";
import {
  existsSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { dirname, join, relative, sep } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SOURCE = join(ROOT, "docs/helix");
const DEST = join(ROOT, "docs/website/content/generated");

const ACTIVITIES = {
  "00-discover": "Discover",
  "01-frame": "Frame",
  "02-design": "Design",
  "03-test": "Test",
  "04-build": "Build",
  "05-deploy": "Deploy",
  "06-iterate": "Iterate",
};

const FRONTMATTER_RE = /^---\r?\n([\s\S]*?)\r?\n---\r?\n/;
const H1_RE = /^#\s+(.+?)\s*$/m;

function splitFrontmatter(text) {
  const m = text.match(FRONTMATTER_RE);
  if (!m) return { fm: "", body: text };
  return { fm: m[1], body: text.slice(m[0].length) };
}

function extractTitle(body, fallback) {
  const m = body.match(H1_RE);
  return m ? m[1].trim() : fallback.replace(/[-_]/g, " ");
}

function yamlDq(s) {
  return `"${String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
}

function walkMd(dir, base = dir) {
  const out = [];
  if (!existsSync(dir)) return out;
  for (const name of readdirSync(dir).sort()) {
    if (name.startsWith(".")) continue;
    const p = join(dir, name);
    const st = statSync(p);
    if (st.isDirectory()) out.push(...walkMd(p, base));
    else if (name.endsWith(".md")) out.push(p);
  }
  return out;
}

function collect() {
  const records = [];
  for (const [activityKey, activityLabel] of Object.entries(ACTIVITIES)) {
    const activityDir = join(SOURCE, activityKey);
    if (!existsSync(activityDir)) continue;
    for (const src of walkMd(activityDir)) {
      const rel = relative(activityDir, src);
      const parts = rel.split(sep);
      const slug = parts[parts.length - 1].replace(/\.md$/, "");
      const collection = parts.length > 1 ? parts[0] : null;
      const destRel = parts.join("/");
      const text = readFileSync(src, "utf8");
      const { body } = splitFrontmatter(text);
      const title = extractTitle(body, slug);
      records.push({
        src,
        destRel,
        activityKey,
        activityLabel,
        collection,
        slug,
        title,
        supporting: /naming-exploration|prior-art-research|examples\//.test(
          relative(SOURCE, src),
        ),
      });
    }
  }
  return records;
}

function renderPage(rec) {
  const text = readFileSync(rec.src, "utf8");
  const { fm, body } = splitFrontmatter(text);
  const relSource = relative(SOURCE, rec.src).split(sep).join("/");
  const lines = [
    "---",
    `title: ${yamlDq(rec.title)}`,
    `slug: ${rec.slug}`,
    `activity: ${yamlDq(rec.activityLabel)}`,
    `source: ${yamlDq(relSource)}`,
    "generated: true",
    `supporting: ${rec.supporting ? "true" : "false"}`,
  ];
  if (rec.collection) lines.push(`collection: ${yamlDq(rec.collection)}`);
  lines.push("---", "");
  lines.push(
    `> **Generated from HELIX docs.** Source: \`docs/helix/${relSource}\`. Edit the source, not this file or \`site/\` HTML.`,
    "",
  );
  if (fm.trim()) {
    lines.push("<details><summary>Source frontmatter</summary>", "", "```yaml", fm.trim(), "```", "", "</details>", "");
  }
  lines.push(body.replace(/^\uFEFF/, "").replace(/^\n+/, ""));
  if (!lines[lines.length - 1].endsWith("\n")) lines.push("");
  return lines.join("\n");
}

function renderArtifactsIndex(records) {
  const lines = [
    "---",
    'title: "Artifacts"',
    "generated: true",
    "---",
    "",
    "# Artifacts",
    "",
    "Generated from the project's HELIX tree (`docs/helix/`). Each page mirrors a governing document. Grouped by activity (when you need the artifact in the loop).",
    "",
    "_Auto-generated by `scripts/publish-artifacts.mjs`._",
    "",
  ];
  for (const [activityKey, activityLabel] of Object.entries(ACTIVITIES)) {
    const members = records.filter((r) => r.activityKey === activityKey);
    if (!members.length) continue;
    lines.push(`## ${activityLabel}`, "");
    const top = members.filter((r) => !r.collection);
    const byCol = new Map();
    for (const r of members.filter((r) => r.collection)) {
      if (!byCol.has(r.collection)) byCol.set(r.collection, []);
      byCol.get(r.collection).push(r);
    }
    for (const r of top.sort((a, b) => a.slug.localeCompare(b.slug))) {
      const href = r.destRel.replace(/\.md$/, "").toLowerCase();
      const tag = r.supporting ? " _(supporting)_" : "";
      lines.push(`- [${r.title}](${href}/)${tag}`);
    }
    for (const [col, items] of [...byCol.entries()].sort()) {
      lines.push(`- **${col}**`);
      for (const r of items.sort((a, b) => a.slug.localeCompare(b.slug))) {
        const href = r.destRel.replace(/\.md$/, "").toLowerCase();
        lines.push(`  - [${r.title}](${href}/)`);
      }
    }
    lines.push("");
  }
  return lines.join("\n");
}

function renderGlossary() {
  // Pull key terms from claim-system naming section if present; else static core set.
  const claimPath = join(SOURCE, "02-design/claim-system.md");
  const designPath = join(SOURCE, "02-design/DESIGN.md");
  const terms = [
    {
      term: "Colophon (colo)",
      def: "Declared recipe of how the work was made: composition plus ingredients. Short form: colo.",
    },
    {
      term: "Composition",
      def: "Coarse state: human-authored, mixed, or model-primary.",
    },
    {
      term: "Ingredient",
      def: "One model, tool, human role, or other line item on the colophon.",
    },
    {
      term: "Claim",
      def: "JSON document binding subjects (content digests) to a colophon and issuer metadata.",
    },
    {
      term: "Attestation",
      def: "Claim payload plus cryptographic signature by an issuer key.",
    },
    {
      term: "Seal / mark",
      def: "Visual Innsigle badge; one family for all composition states.",
    },
    {
      term: "Signet",
      def: "Act of signing; optional cryptographic depth over an unsigned declaration.",
    },
    {
      term: "Issuer",
      def: "Person or house whose key signs the claim.",
    },
  ];
  const lines = [
    "---",
    'title: "Glossary"',
    "generated: true",
    "---",
    "",
    "# Glossary",
    "",
    "Generated term list for the Innsigle public site. Definitions align with",
    "`docs/helix/02-design/claim-system.md` and DESIGN.md.",
    "",
  ];
  for (const t of terms) {
    lines.push(`## ${t.term}`, "", t.def, "");
  }
  if (existsSync(claimPath)) {
    lines.push(
      "## Sources",
      "",
      `- [Claim system](../artifacts/claim-system/)`,
      existsSync(designPath) ? `- [DESIGN.md](../artifacts/design/)` : "",
      "",
    );
  }
  return lines.filter(Boolean).join("\n");
}

function main() {
  rmSync(DEST, { recursive: true, force: true });
  mkdirSync(join(DEST, "artifacts"), { recursive: true });

  const records = collect();
  for (const rec of records) {
    const dest = join(DEST, "artifacts", rec.destRel.toLowerCase());
    mkdirSync(dirname(dest), { recursive: true });
    writeFileSync(dest, renderPage(rec));
  }
  writeFileSync(join(DEST, "artifacts/index.md"), renderArtifactsIndex(records));
  writeFileSync(join(DEST, "glossary.md"), renderGlossary());

  const fingerprint = createHash("sha256")
    .update(records.map((r) => relative(SOURCE, r.src)).sort().join("\n"))
    .digest("hex")
    .slice(0, 12);
  console.error(
    `publish-artifacts: ${records.length} pages → ${relative(ROOT, DEST)} (${fingerprint})`,
  );
}

main();
